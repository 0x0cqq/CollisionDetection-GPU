use std::ops::Range;

use crate::texture;

pub trait Vertex {
    fn desc<'a>() -> wgpu::VertexBufferLayout<'a>;
}

/// “ModelVertex”结构表示 3D 模型中的顶点，包含位置、纹理坐标、法线、切线和双切线数据。
///
/// Properties:
///
/// * `position`: 表示顶点在空间中的位置的 3D 坐标。
/// * `tex_coords`: `tex_coords` 属性表示顶点的纹理坐标。纹理坐标用于将 2D 纹理映射到 3D
/// 模型上。它们指定如何将纹理应用到模型的表面。在本例中，“tex_coords”是一个包含两个的数组
/// * `normal`: 法线属性表示顶点的法线向量。它是一个在该顶点垂直于表面的 3D 矢量。法线用于照明计算，以确定光线如何与表面相互作用。
/// * `tangent`: 切向量是垂直于表面法向量且位于纹理坐标平面内的向量。它在法线贴图中用于计算 3D 模型表面的光照。
/// * `bitangent`: 双切线是垂直于顶点的法线向量和切线向量的向量。它在法线贴图中用于计算表面上的正确光照。
#[repr(C)]
#[derive(Copy, Clone, Debug, bytemuck::Pod, bytemuck::Zeroable)]
pub struct ModelVertex {
    pub position: [f32; 3],
    pub tex_coords: [f32; 2],
    pub normal: [f32; 3],
    pub tangent: [f32; 3],
    pub bitangent: [f32; 3],
}

impl Vertex for ModelVertex {
    fn desc<'a>() -> wgpu::VertexBufferLayout<'a> {
        use std::mem;
        wgpu::VertexBufferLayout {
            array_stride: mem::size_of::<ModelVertex>() as wgpu::BufferAddress,
            step_mode: wgpu::VertexStepMode::Vertex,
            attributes: &[
                wgpu::VertexAttribute {
                    offset: 0,
                    shader_location: 0,
                    format: wgpu::VertexFormat::Float32x3,
                },
                wgpu::VertexAttribute {
                    offset: mem::size_of::<[f32; 3]>() as wgpu::BufferAddress,
                    shader_location: 1,
                    format: wgpu::VertexFormat::Float32x2,
                },
                wgpu::VertexAttribute {
                    offset: mem::size_of::<[f32; 5]>() as wgpu::BufferAddress,
                    shader_location: 2,
                    format: wgpu::VertexFormat::Float32x3,
                },
                // Tangent and bitangent
                wgpu::VertexAttribute {
                    offset: mem::size_of::<[f32; 8]>() as wgpu::BufferAddress,
                    shader_location: 3,
                    format: wgpu::VertexFormat::Float32x3,
                },
                wgpu::VertexAttribute {
                    offset: mem::size_of::<[f32; 11]>() as wgpu::BufferAddress,
                    shader_location: 4,
                    format: wgpu::VertexFormat::Float32x3,
                },
            ],
        }
    }
}

/// “Material”结构表示具有名称、漫反射和法线纹理以及用于 GPU 绑定的绑定组的材质。
///
/// Properties:
///
/// * `name`: 代表材质名称的字符串。
/// * `diffuse_texture`: `diffuse_texture` 属性的类型为 `texture::Texture`。它表示用于材质漫反射颜色的纹理。
/// * `normal_texture`: `normal_texture` 属性的类型为
/// `texture::Texture`。它表示用于材质中法线贴图的纹理。法线贴图是计算机图形学中使用的一种技术，通过模拟小凹凸和缝隙来向表面添加细节。
/// * `bind_group`: `bind_group` 是 `wgpu::BindGroup` 类型的属性。它在 WebGPU API 的上下文中使用，WebGPU API 是用于 Web
/// 的低级图形和计算 API。 “BindGroup”表示绑定在一起并由着色器使用的资源集合
pub struct Material {
    pub name: String,
    pub diffuse_texture: texture::Texture,
    pub normal_texture: texture::Texture,
    pub bind_group: wgpu::BindGroup,
}

impl Material {
    pub fn new(
        device: &wgpu::Device,
        name: &str,
        diffuse_texture: texture::Texture,
        normal_texture: texture::Texture,
        layout: &wgpu::BindGroupLayout,
    ) -> Self {
        let bind_group = device.create_bind_group(&wgpu::BindGroupDescriptor {
            layout,
            entries: &[
                wgpu::BindGroupEntry {
                    binding: 0,
                    resource: wgpu::BindingResource::TextureView(&diffuse_texture.view),
                },
                wgpu::BindGroupEntry {
                    binding: 1,
                    resource: wgpu::BindingResource::Sampler(&diffuse_texture.sampler),
                },
                wgpu::BindGroupEntry {
                    binding: 2,
                    resource: wgpu::BindingResource::TextureView(&normal_texture.view),
                },
                wgpu::BindGroupEntry {
                    binding: 3,
                    resource: wgpu::BindingResource::Sampler(&normal_texture.sampler),
                },
            ],
            label: Some(name),
        });

        Self {
            name: String::from(name),
            diffuse_texture,
            normal_texture,
            bind_group,
        }
    }
}

/// “Mesh”结构表示具有名称、顶点和索引缓冲区、元素数量和材质索引的 3D 网格。
///
/// Properties:
///
/// * `name`: 表示网格名称的字符串。
/// * `vertex_buffer`: `vertex_buffer` 属性的类型为 `wgpu::Buffer`。用于存储网格体的顶点数据，如每个顶点的位置、法线、纹理坐标等。该缓冲区通常由
/// WebGPU 或 Vulkan 等图形 API 创建和管理。
/// * `index_buffer`: “index_buffer”属性是一个缓冲区，用于存储组成网格的顶点的索引。它用于确定渲染顶点以创建所需形状的顺序。
/// * `num_elements`: “num_elements”属性表示网格中的元素数量。这通常是指网格中的顶点或索引的数量，具体取决于网格的定义方式。
/// * `material`: “material”属性的类型为“usize”，表示分配给网格的材质的索引。
pub struct Mesh {
    pub name: String,
    pub vertex_buffer: wgpu::Buffer,
    pub index_buffer: wgpu::Buffer,
    pub num_elements: u32,
    pub material: usize,
}

/// “Model”结构表示 3D 模型并包含网格和材质的集合。
///
/// Properties:
///
/// * `meshes`: 网格对象的向量。每个 Mesh 代表模型中的一个 3D 对象，由顶点、法线、纹理坐标和其他属性组成。
/// * `materials`: “materials”属性是“Material”对象的向量。它存储用于渲染模型中的网格的材质。
pub struct Model {
    pub meshes: Vec<Mesh>,
    pub materials: Vec<Material>,
}

pub trait DrawModel<'a> {
    fn draw_mesh(
        &mut self,
        mesh: &'a Mesh,
        material: &'a Material,
        camera_bind_group: &'a wgpu::BindGroup,
        light_bind_group: &'a wgpu::BindGroup,
    );
    fn draw_mesh_instanced(
        &mut self,
        mesh: &'a Mesh,
        material: &'a Material,
        instances: Range<u32>,
        camera_bind_group: &'a wgpu::BindGroup,
        light_bind_group: &'a wgpu::BindGroup,
    );

    fn draw_model(
        &mut self,
        model: &'a Model,
        camera_bind_group: &'a wgpu::BindGroup,
        light_bind_group: &'a wgpu::BindGroup,
    );
    fn draw_model_instanced(
        &mut self,
        model: &'a Model,
        instances: Range<u32>,
        camera_bind_group: &'a wgpu::BindGroup,
        light_bind_group: &'a wgpu::BindGroup,
    );
    fn draw_model_instanced_with_material(
        &mut self,
        model: &'a Model,
        material: &'a Material,
        instances: Range<u32>,
        camera_bind_group: &'a wgpu::BindGroup,
        light_bind_group: &'a wgpu::BindGroup,
    );
}

impl<'a, 'b> DrawModel<'b> for wgpu::RenderPass<'a>
where
    'b: 'a,
{
    fn draw_mesh(
        &mut self,
        mesh: &'b Mesh,
        material: &'b Material,
        camera_bind_group: &'b wgpu::BindGroup,
        light_bind_group: &'b wgpu::BindGroup,
    ) {
        self.draw_mesh_instanced(mesh, material, 0..1, camera_bind_group, light_bind_group);
    }

    fn draw_mesh_instanced(
        &mut self,
        mesh: &'b Mesh,
        material: &'b Material,
        instances: Range<u32>,
        camera_bind_group: &'b wgpu::BindGroup,
        light_bind_group: &'b wgpu::BindGroup,
    ) {
        self.set_vertex_buffer(0, mesh.vertex_buffer.slice(..));
        self.set_index_buffer(mesh.index_buffer.slice(..), wgpu::IndexFormat::Uint32);
        self.set_bind_group(0, &material.bind_group, &[]);
        self.set_bind_group(1, camera_bind_group, &[]);
        self.set_bind_group(2, light_bind_group, &[]);
        self.draw_indexed(0..mesh.num_elements, 0, instances);
    }

    fn draw_model(
        &mut self,
        model: &'b Model,
        camera_bind_group: &'b wgpu::BindGroup,
        light_bind_group: &'b wgpu::BindGroup,
    ) {
        self.draw_model_instanced(model, 0..1, camera_bind_group, light_bind_group);
    }

    fn draw_model_instanced(
        &mut self,
        model: &'b Model,
        instances: Range<u32>,
        camera_bind_group: &'b wgpu::BindGroup,
        light_bind_group: &'b wgpu::BindGroup,
    ) {
        for mesh in &model.meshes {
            let material = &model.materials[mesh.material];
            self.draw_mesh_instanced(
                mesh,
                material,
                instances.clone(),
                camera_bind_group,
                light_bind_group,
            );
        }
    }

    fn draw_model_instanced_with_material(
        &mut self,
        model: &'b Model,
        material: &'b Material,
        instances: Range<u32>,
        camera_bind_group: &'b wgpu::BindGroup,
        light_bind_group: &'b wgpu::BindGroup,
    ) {
        for mesh in &model.meshes {
            self.draw_mesh_instanced(
                mesh,
                material,
                instances.clone(),
                camera_bind_group,
                light_bind_group,
            );
        }
    }
}

